# include<stdio.h>

int main() {

//Print integer variable- %d
    int age = 18;
    printf("age is %d", age);

//Print real number variable- %f
    float pi = 3.14;
    printf("value is %f", pi);

//Print character variable- %c
    char star = '*';
    printf("star looks like %c", star);


    return 0;
}